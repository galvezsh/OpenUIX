package kernel;

import gui.WindowsManager;

public class Kernel {

	//////////////////////////////////////////
	// ATTRIBUTES ////////////////////////////
	//////////////////////////////////////////
	
	private final Configuration config;

	//////////////////////////////////////////
	// CONSTRUCTOR ///////////////////////////
	//////////////////////////////////////////
	
	public Kernel() {
		this.config = new Configuration();
		new WindowsManager(this);
	}
	
	//////////////////////////////////////////
	// METHODS ///////////////////////////////
	//////////////////////////////////////////

	public static void main(String[] args) {
		new Kernel();
	}
	
	//////////////////////////////////////////
	// GET/SET ///////////////////////////////
	//////////////////////////////////////////
	
	public Configuration getConfiguration() {
		return config;
	}
	
}
